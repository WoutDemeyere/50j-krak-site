function toggleTabs() {
    let tabTrigger = document.querySelectorAll(".js-tab-list__item");
    let tabInfo = document.querySelector(".js-tab-info-container");

    for (let i = 0; i < tabTrigger.length; i++) {

        tabTrigger[i].addEventListener("click", function () {

            tabInfo.innerHTML = tab_info[i];
            console.log(tabInfo)

            if (!tabTrigger[i].classList.contains("c-tab-selected")) {
                tabTrigger[i].classList.add("c-tab-selected");

                for (let j = 0; j < tabTrigger.length; j++) {
                    if (j != i) {
                        tabTrigger[j].classList.remove("c-tab-selected");
                    }
                }
            }
        })
    }


}

